		<h2>Shuffling</h2>
		<ol>
			<li>Place your left hand over the deck of cards, concentrating on your question. <br /><img src="pre1.jpg" /></li>
		<li>Spread out the cards and shuffle them thoroughly using your left hand. <br /><img src="pre2.jpg" /></li>
		<li>When you feel that the cards had been shuffled enough, gather the cards back into the deck. <br /><img src="pre3.jpg" /><br />
			*Primarily, shuffling of the cards are done with the left hand. However, when you are gathering the cards, you can use both hands.</li>
		<li>Next, you have to cut the deck. With your left hand, lift the entire deck. From the bottom, divide the deck into 5 smaller decks. <br /><img src="pre4.jpg" /><br />
			*the number of cards in each smaller deck need not be the same. </li>
			<li>With your left hand, re-stack the 5 smaller decks together in the order of your preference. <br /><img src="pre5.jpg" /></li>
			<li>Repeat the entire procedure (i.e. shuffle twice), after which you would have finished the shuffling.</li>
		</ol>
